# -----------------------------------------
# 1. vi 실행 / 종료
# -----------------------------------------

# vi [파일명] : vi 편집기로 파일 열기
#  - 파일이 없으면 새 파일로 연다.
# 예) vi main.sh
vi main.sh

# 아래 명령들은 모두 "명령 모드"에서 입력해야 한다.
# (입력 모드에서 ESC를 눌러 먼저 명령 모드로 전환)

# :wq  : 저장(write) 후 종료(quit)
# :q   : 저장하지 않고 종료 (변경 사항 없을 때만)
# :q!  : 저장하지 않고 강제 종료 (변경 사항 버림)
# :w   : 저장만 하고 종료는 안 함
# 명령 예) :wq  입력 후 Enter
# (콜론(:)을 치는 순간 화면 맨 아래에 커서가 이동한다)


# -----------------------------------------
# 2. 모드 전환 (입력 / 명령 모드)
# -----------------------------------------

# vi는 크게 두 가지 모드:
#  - 명령 모드(Command mode)  : 이동, 복사/삭제, 저장/종료 등
#  - 입력 모드(Insert mode)   : 실제 텍스트 입력

# ESC : 언제든지 입력 모드 → 명령 모드 전환
#  - "지금 무슨 모드인지 모르겠다" 싶으면 ESC 한 번 눌러서
#    명령 모드로 돌아온 다음 다시 시작하면 됨.

# 입력 모드 진입 키(명령 모드에서 사용):

# i : 현재 커서 위치에서부터 입력 시작
# a : 현재 커서 다음 칸부터 입력 시작 (append)
# o : 현재 줄의 "아래 줄"을 새로 만들고, 그 줄에서 입력 시작

# (참고: 자주 쓰는 추가 키)
# I : 현재 줄의 맨 앞에서 입력 시작
# A : 현재 줄의 맨 끝에서 입력 시작
# O : 현재 줄의 "위에 줄"을 새로 만들고, 그 줄에서 입력 시작


# -----------------------------------------
# 3. 기본 편집 명령 (삭제 / 복사 / 붙여넣기)
# -----------------------------------------

# 모두 "명령 모드"에서 사용.

# x  : 커서가 있는 글자 1개 삭제
# dd : 현재 줄 한 줄 삭제
# yy : 현재 줄 한 줄 복사(yank)
# p  : 복사/삭제한 내용을 다음 줄(또는 커서 뒤)에 붙여넣기

# 예시 흐름:
#  - yy      현재 줄 복사
#  - p       아래 줄에 붙여넣기
#  - dd      현재 줄 삭제


# -----------------------------------------
# 4. 이동 관련 명령
# -----------------------------------------

# G  : 파일 맨 끝 줄로 이동
# gg : 파일 맨 처음 줄로 이동

# Ctrl + f : 한 페이지 아래로 이동 (page forward)
# Ctrl + b : 한 페이지 위로 이동 (page backward)

# (추가로 자주 쓰는 이동)
# 0    : 현재 줄 맨 앞으로 이동 (숫자 0)
# $    : 현재 줄 맨 끝으로 이동
# w    : 다음 단어의 시작으로 이동
# b    : 이전 단어의 시작으로 이동


# -----------------------------------------
# 5. 검색(Search)
# -----------------------------------------

# /[검색어] + Enter : 아래 방향으로 검색
#  - 예) /hello  + Enter  → "hello" 문자열을 아래로 찾아감
#
# 검색 후:
#  - n : 다음 검색 결과로 이동
#  - N : 이전 검색 결과로 이동

# 예시:
#  /error    → "error"를 아래쪽으로 검색
#  n         → 다음 "error"로
#  N         → 이전 "error"로


# -----------------------------------------
# 6. 자주 쓰는 최소한의 vi 사용 루틴 예시
# -----------------------------------------

# 1) 파일 열기
#    vi app.log
#
# 2) 파일 내용 조금 수정
#    - 방향키로 이동
#    - i 또는 a 또는 o 로 입력 모드 진입
#    - 내용 입력/수정
#    - ESC 로 명령 모드 복귀
#
# 3) 저장 후 종료
#    :wq   입력 후 Enter
#
# 4) 잘못 수정해서 저장 안 하고 나가고 싶을 때
#    :q!   입력 후 Enter