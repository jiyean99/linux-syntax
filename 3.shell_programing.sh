# -----------------------------------------
# 0. 파일 종류와 실행 파일 개념
# -----------------------------------------

# 리눅스에서 파일은 크게:
#  - 편집 가능한 파일(텍스트 파일 등)
#  - 실행 파일(바이너리, 스크립트 등)
# 로 나눠 생각할 수 있다.
#
# 예)
#  - IntelliJ.exe  : 실행 파일 (윈도우 기준, 더블클릭/명령으로 실행)
#  - index.txt     : 내용 편집용 텍스트 파일
#
# 리눅스에서는 확장자가 실행 여부를 결정하는 것이 아니라,
#  - "실행 권한(x)"이 있느냐
#  - 그리고 그 파일이 실행 가능한 형식이냐
# 가 더 중요하다.


# -----------------------------------------
# 1. Shell 스크립트 기본 규칙
# -----------------------------------------

# 1) 확장자
#  - 관례적으로 .sh 확장자를 많이 사용한다.
#  - 예) myscript.sh

# 2) 실행 방법
#  - 보통 현재 디렉터리의 스크립트는 ./로 실행:
#      ./myscript.sh
#  - 그냥 myscript.sh 라고만 치면 PATH에 없어서 실행 안 될 수 있다.

# 3) 실행 권한 필요
#  - 실행 전에 실행 권한(x)을 줘야 한다:
#      chmod u+x myscript.sh
#    (현재 사용자(u)에게 실행 권한 추가(+x))

# 4) 스크립트 상단 shebang (실제 작성 시 권장)
#  - 예)
#      #!/bin/bash
#    이 파일이 bash로 실행되어야 한다는 표시.


# -----------------------------------------
# 2. if 문 기본 문법
# -----------------------------------------

# 기본 형태:
# if [ 조건 ]; then
#     # 조건이 참일 때 실행할 명령
# elif [ 다른 조건 ]; then
#     # 첫 번째 조건이 거짓이고, 다른 조건이 참일 때 실행
# else
#     # 모든 조건이 거짓일 때 실행
# fi
#
# 비교 연산 예:
#  - 정수 비교: -eq(==), -ne(!=), -gt(>), -lt(<), -ge(>=), -le(<=)
#    예) [ 1 -gt 2 ]  → 1 > 2 ?
#  - 문자열 비교: =, !=, -z(길이 0), -n(길이 > 0) 등


# ===== if 예제 1: 숫자 비교 =====

# gt: greater than (보다 큰)
# lt: less than   (보다 작은)

if [ 1 -gt 2 ]; then
    echo "hello world"
else
    echo "bye world"
fi


# -----------------------------------------
# 3. 변수 선언 및 파일/디렉터리 검사
# -----------------------------------------

# 변수 선언:
#   변수명=값        (= 양쪽에 공백 없이)
# 사용:
#   $변수명          (문자열 안에서는 "$변수명" 형태 추천)

file_name="first_file.txt"

# 파일 존재 여부 검사:
#  -f : 일반 파일이 존재하는지 확인
#  -d : 디렉터리 존재 여부 확인에 사용
#
# 예)
#   [ -f "$file_name" ] : 해당 이름의 "파일"이 있으면 참
#   [ -d "$dir_name" ]  : 해당 이름의 "디렉터리"가 있으면 참

if [ -f "$file_name" ]; then
    echo "$file_name exists!"
else
    echo "$file_name does not exist!"
fi


# -----------------------------------------
# 4. for 문 기본 문법
# -----------------------------------------

# 기본 형태:
# for 변수 in 항목들; do
#     # 반복할 명령
# done
#
# - 항목들 자리에는:
#   - 직접 나열한 값들:   for x in a b c
#   - 범위 표현:           for x in {1..10}
#   - 명령 결과:           for x in $(ls)
#   - 글로브 패턴:         for x in *.txt  등등


# ===== for 예제 1: 1부터 100까지 출력 =====

# {1..100} : 1부터 100까지 시퀀스 생성 (bash brace expansion)
for a in {1..100}; do
    echo "hello world $a"
done


# ===== for 예제 2: 현재 디렉터리의 파일/디렉터리 목록 출력 =====

# * : 현재 디렉터리의 모든 항목 (숨김 파일 제외)
for a in *; do
    echo "$a"
done


# ===== for 예제 3: 카운트 증가 =====

count=100

# {1..100} 으로 표기 (중간 점은 두 개)
for a in {1..100}; do
    # let 은 산술 연산용 내장 명령
    # 다른 표기: count=$((count + 1))
    let count=count+1
done

echo "count value is $count"